
def reverse_sauce(encrypted_char):
    left = encrypted_char >> 4
    right = encrypted_char & 0x0F
    for _ in range(5):
        left, right = right ^ ((left + 0x1337) & 0x0F), left
    original_char = (left << 4) | right
    print(chr(original_char),end='')

def reverse_meat(encrypted_char):
    original_char = ((encrypted_char >> 3) | (encrypted_char << 5)) & 0xFF
    print(chr(original_char),end='')

def reverse_pasta(encrypted_char):
    original_char = encrypted_char
    for i in reversed(range(5)):
        original_char = ((original_char >> (i + 3))) | (original_char << (8 - (i + 3))) & 0xFF
        original_char ^= (0x1337 - i) & 0xFF
    print(chr(original_char),end='')
        
def reverse_ricotta(encrypted_char):
    original_char =  ((encrypted_char >> 4) | (encrypted_char << 4)) & 0xFF
    print(chr(original_char),end='')

def reverse_mozzarella(encrypted_char):
    original_char = encrypted_char
    for i in reversed(range(5)):
        original_char = ((original_char >> (i + 1)) | (original_char << (8 - (i + 1)))) & 0xFF
        original_char ^= (0x1337 + i) & 0xFF
    print(chr(original_char),end='')

def reverse_parmesan(encrypted_char):
    original_char = ((encrypted_char >> 6) | (encrypted_char << 2)) & 0xFF
    original_char = original_char ^ 0x1f
    print(chr(original_char),end='')

